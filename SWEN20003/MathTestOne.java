public class MathTestOne {
    public static void main(String args[]) {
        /* Initial values */
        int x = 3;
        int y = 5;
        
        /* Output testing*/
        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x / (y * 1.0));
        System.out.println(x % y);
    }
}